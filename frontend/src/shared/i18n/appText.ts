function getSupportedLocale(locale: string): SupportedLocale {
  if (locale.startsWith('pt')) {
    return 'pt-BR';
  }

  return 'en-US';
}

export function translateAppText(
  key: string,
  values?: TranslationValues,
  locale = getDisplayPreferences().locale,
) {
  const supportedLocale = getSupportedLocale(locale);
  const template = translations[supportedLocale][key] ?? translations['en-US'][key] ?? key;

  if (!values) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_match, token) => String(values[token] ?? ''));
}
